__attribute__((used, section(".limine_requests")))
volatile struct limine_framebuffer_request framebuffer_request = {
    .id = LIMINE_FRAMEBUFFER_REQUEST,
    .revision = 0
};
void clear_framebuffers(uint32_t color) {
    if (framebuffer_request.response == NULL || framebuffer_request.response->framebuffer_count == 0) {
        return;
    }

    for (size_t i = 0; i < framebuffer_request.response->framebuffer_count; i++) {
        struct limine_framebuffer *fb = framebuffer_request.response->framebuffers[i];
        uint32_t *fb_ptr = (uint32_t *)fb->address;
        size_t pixels = fb->width * fb->height;

        for (size_t p = 0; p < pixels; p++) {
            fb_ptr[p] = color;
        }
    }
}





static struct limine_framebuffer *fb;
static int fb_cursor_x = 0;
static int fb_cursor_y = 0;

void fb_terminal_init(struct limine_framebuffer *fb_ptr) {
    fb = fb_ptr;
    fb_cursor_x = 0;
    fb_cursor_y = 0;
}

void fb_put_pixel(int x, int y, uint32_t color) {
    if (!fb || x >= (int)fb->width || y >= (int)fb->height || x < 0 || y < 0) return;
    uint32_t *addr = (uint32_t*)((uintptr_t)fb->address + fb->pitch * y + 4 * x);
    *addr = color;
}

void fb_draw_char(char c, int px, int py, uint32_t fg, uint32_t bg) {
    if (c < 0 || c >= 128) return;
    for (int y = 0; y < 8; y++) {
        uint8_t row = font8x8_basic[(int)c][y];
        for (int x = 0; x < 8; x++) {
            uint32_t color = (row & (1 << x)) ? fg : bg;
            fb_put_pixel(px + x, py + y, color);
        }
    }
}

void fb_terminal_putchar(char c) {
    if (c == '\n') {
        fb_cursor_x = 0;
        fb_cursor_y += 8;
        return;
    }

    fb_draw_char(c, fb_cursor_x, fb_cursor_y, 0xFFFFFFFF, 0x00000000);
    fb_cursor_x += 8;

    if (fb_cursor_x + 8 >= (int)fb->width) {
        fb_cursor_x = 0;
        fb_cursor_y += 8;
    }
}




void fb_terminal_writestring(const char* str) {
    while (*str) {
        fb_terminal_putchar(*str++);
    }
}

void print_hex64(uint64_t val) {
    char hex_chars[] = "0123456789ABCDEF";
    char buffer[17];
    buffer[16] = '\0';
    for (int i = 15; i >= 0; i--) {
        buffer[i] = hex_chars[val & 0xF];
        val >>= 4;
    }
    fb_terminal_writestring("0x");
    fb_terminal_writestring(buffer);
}
